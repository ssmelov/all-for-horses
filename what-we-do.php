					<h2>Our aims</h2>
					<p>An increasing number of good, previously well-loved family riding horses and ponies are still being 
						sent for meat or abandoned every year, and this number is rising all the time.</p> 
					<p>While we sadly cannot save all the horses in this predicament, our aim is to take in and provide 
						care, rehabilitation and rehoming facilities so that some of these horses can have useful lives and
						be loved and cared for again. 
					</p>
					<p>
						Despite the UK being a nation of animal lovers, ill treatment, neglect and ignorance are still part of 
						many horses lives, and so our work also focuses on promoting cruelty-free humane training 
						methods and horse-friendly care practices. We help do this by providing advice and information 
						to horse owners and the public, and by organising events and horse training demonstrations.
					</p>
					
					<div class="small-sep"></div>
					
					<h2>Rehabilitation</h2>
					<p>Horses taken in are assessed both for health and behavioural issues and a written plan prepared for each horse, in addition to the health record. Many rescue horses have simply fallen on hard times and are in need of some TLC, and following careful monitoring will usually recover easily.</p>
					<p>Depending on the circumstances, some have physical issues and need longer to recuperate, while others have been traumatised and need remedial training and initially expert handling to help them recover from their fears.</p>
					<p>Owners are mostly honest about their horses’ needs and abilities, however some desperate owners have been known omit important details when they need “rid” of a horse, so it is essential that we fully assess each horse ourselves.</p>
					<p>Once a horse is fit and well, and has received any training or treatment needed, he or she will be offered for rehoming on a long term loan basis.</p>					
					<p>Our aim wherever possible is to provide training and  schooling so that horses go out on loan suitable  for ridden work, rather  than as non-ridden companions (although we do have companions available too), as there is  more demand  for rideable horses.</p>								
